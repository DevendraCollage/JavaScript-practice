//todo: Operators
//* 1. Arithmetic Operators
let num1 = 10;
let num2 = 5;
console.log(num1 + num2); //? Addition
console.log(num1 - num2); //? Subtraction
console.log(num1 * num2); //? Multiplication
console.log(num1 ** num2); //? Exponentiation
console.log(num1 / num2); //? Division
console.log(num1 % num2); //? Modulus
console.log(num1++); //? Increment
console.log(num2--); //? Decrement

//* 2. Assignment Operator
console.log((num1 = 13));
console.log((num1 += 5));
console.log((num1 -= 2));
console.log((num2 *= 2));
console.log((num1 /= 2));
console.log((num2 %= 2));
console.log((num2 **= 2));

//* 3. Comparison Operator
console.log(num1 == num2); //? Equal to
console.log(num1 != num1); //? Not Equal
console.log(num1 === num2); //? Equal value and data type
console.log(num1 !== num2); //? Not equal value or not equal data type
console.log(num1 > num2); //? Greater Than
console.log(num1 < num2); //? Less Than
console.log(num1 >= num2); //? Greater than or equal to
console.log(num1 <= num2); //? Less than or equal to
console.log(num1 ? "True" : "False"); //? Ternary operator

//* 4. Logical Operators
console.log(num1 && num2); //? Logical and
console.log(num1 || num2); //? Logical or
!true; //? Logical not
